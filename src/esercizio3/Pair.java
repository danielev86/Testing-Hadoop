package esercizio3;

public class Pair {
	public String str;
    public Integer count;
    
    public Pair(String str, Integer count) {
      this.str = str;
      this.count = count;
    }

	public String getStr() {
		return str;
	}

	public void setStr(String str) {
		this.str = str;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}
    
    

}
